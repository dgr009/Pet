package com.ppp.vo;

public class Vet {
	   int vetNo;
	   String vetName;
	   String vetIntroduce;
	   int hospitalNo;
	public int getVetNo() {
		return vetNo;
	}
	public void setVetNo(int vetNo) {
		this.vetNo = vetNo;
	}
	public String getVetName() {
		return vetName;
	}
	public void setVetName(String vetName) {
		this.vetName = vetName;
	}
	public String getVetIntroduce() {
		return vetIntroduce;
	}
	public void setVetIntroduce(String vetIntroduce) {
		this.vetIntroduce = vetIntroduce;
	}
	public int getHospitalNo() {
		return hospitalNo;
	}
	public void setHospitalNo(int hospitalNo) {
		this.hospitalNo = hospitalNo;
	}
	   
}
