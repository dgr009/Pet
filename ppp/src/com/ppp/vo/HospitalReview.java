package com.ppp.vo;

public class HospitalReview {
	int hospitalReviewNo;
	float hospitalReviewScore;
	String hospitalReviewEpilogue;
	int hospitalNo;
	int memberNo;
	public int getHospitalReviewNo() {
		return hospitalReviewNo;
	}
	public void setHospitalReviewNo(int hospitalReviewNo) {
		this.hospitalReviewNo = hospitalReviewNo;
	}
	public float getHospitalReviewScore() {
		return hospitalReviewScore;
	}
	public void setHospitalReviewScore(float hospitalReviewScore) {
		this.hospitalReviewScore = hospitalReviewScore;
	}
	public String getHospitalReviewEpilogue() {
		return hospitalReviewEpilogue;
	}
	public void setHospitalReviewEpilogue(String hospitalReviewEpilogue) {
		this.hospitalReviewEpilogue = hospitalReviewEpilogue;
	}
	public int getHospitalNo() {
		return hospitalNo;
	}
	public void setHospitalNo(int hospitalNo) {
		this.hospitalNo = hospitalNo;
	}
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	
}
