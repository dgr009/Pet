package com.ppp.vo;

public class BeautyReserveDetail {
	int reserveBeautyNo;
	int memberNo;
	int beautyNo;
	String animalKind;
	float animalWeight;
	public int getReserveBeautyNo() {
		return reserveBeautyNo;
	}
	public void setReserveBeautyNo(int reserveBeautyNo) {
		this.reserveBeautyNo = reserveBeautyNo;
	}
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	public int getBeautyNo() {
		return beautyNo;
	}
	public void setBeautyNo(int beautyNo) {
		this.beautyNo = beautyNo;
	}
	public String getAnimalKind() {
		return animalKind;
	}
	public void setAnimalKind(String animalKind) {
		this.animalKind = animalKind;
	}
	public float getAnimalWeight() {
		return animalWeight;
	}
	public void setAnimalWeight(float animalWeight) {
		this.animalWeight = animalWeight;
	}
	
}
