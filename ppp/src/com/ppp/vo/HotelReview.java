package com.ppp.vo;

public class HotelReview {
	int hotelReviewNo;
	float hotelReviewScore;
	String hotelReviewEpilogue;
	int hotelNo;
	int memberNo;
	public int getHotelReviewNo() {
		return hotelReviewNo;
	}
	public void setHotelReviewNo(int hotelReviewNo) {
		this.hotelReviewNo = hotelReviewNo;
	}
	public float getHotelReviewScore() {
		return hotelReviewScore;
	}
	public void setHotelReviewScore(float hotelReviewScore) {
		this.hotelReviewScore = hotelReviewScore;
	}
	public String getHotelReviewEpilogue() {
		return hotelReviewEpilogue;
	}
	public void setHotelReviewEpilogue(String hotelReviewEpilogue) {
		this.hotelReviewEpilogue = hotelReviewEpilogue;
	}
	public int getHotelNo() {
		return hotelNo;
	}
	public void setHotelNo(int hotelNo) {
		this.hotelNo = hotelNo;
	}
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	
}
